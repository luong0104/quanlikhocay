public class Cay {
    public String macay,tencay,loaicay;
    public Double giathanh;
    public int soluong;

    public Cay(String macay, String tencay, String loaicay, Double giathanh, int soluong) {
        this.macay = macay;
        this.tencay = tencay;
        this.loaicay = loaicay;
        this.giathanh = giathanh;
        this.soluong = soluong;
    }

    public String getMacay() {
        return macay;
    }

    public void setMacay(String macay) {
        this.macay = macay;
    }

    public String getTencay() {
        return tencay;
    }

    public void setTencay(String tencay) {
        this.tencay = tencay;
    }

    public String getLoaicay() {
        return loaicay;
    }

    public void setLoaicay(String loaicay) {
        this.loaicay = loaicay;
    }

    public Double getGiathanh() {
        return giathanh;
    }

    public void setGiathanh(Double giathanh) {
        this.giathanh = giathanh;
    }

    public int getSoluong() {
        return soluong;
    }

    public void setSoluong(int soluong) {
        this.soluong = soluong;
    }
    
    
}
